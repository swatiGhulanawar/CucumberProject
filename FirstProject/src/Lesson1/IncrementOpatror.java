package Lesson1;

public class IncrementOpatror {

	public static void main(String[] args) {
		int x=20;
		
			
		//post increment 
		//x++;
		System.out.println(x++);
		System.out.println(x);
		
		//pre-increment
		int y =27;
		//++y;
		System.out.println(++y);
		System.out.println(y++);
		System.out.println(y);
		
		int a=1;
		a +=5;
		System.out.println(a);
	
	}

}
